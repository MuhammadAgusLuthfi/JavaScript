
// Number Notation menggunakan Hexadecimal diawali dengan (0x)
document.writeln("Hexadecimal :");
document.writeln(0xFF);
document.writeln("</br>");
document.writeln("<hr>");
// Number Notation menggunakan Binary diawali dengan (0b)
document.writeln("Binary :");
document.writeln(0b10101);
document.writeln("</br>");
document.writeln("<hr>");
// Number Notation menggunakan Octal diawali dengan (0o)
document.writeln("Octal :");
document.writeln(0o10);